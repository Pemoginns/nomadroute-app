export function getHostelsByDestination(destinationId: string): Hostel[] {
  return HOSTELS.filter(h => h.destinationId === destinationId)
}

export function getBestHostelForDestination(destinationId: string): Hostel | undefined {
  const hostels = getHostelsByDestination(destinationId)
  return hostels.sort((a, b) => b.rating - a.rating)[0]
}

export function getCheapestHostel(destinationId: string): Hostel | undefined {
  const hostels = getHostelsByDestination(destinationId)
  return hostels.sort((a, b) => a.dormPrice - b.dormPrice)[0]
}
